    private void loadClients() {
        
        Dao dao = new Dao();
        ArrayList <Veiculos> veiculos = dao.todosVeiculos();
        String locando;
        DefaultTableModel model = (DefaultTableModel) tabela.getModel();
        
        
        for(Veiculos v : veiculos){
            if(v.getLocando() == 1)
                locando = "Sim";
            else{
                 locando = "N√£o";
            }
            Object[] dados = {v.getPlaca(), v.getMarca(), v.getModelo() , locando};
            model.addRow(dados);
        
        }
        tabela.setModel(model);
    }
